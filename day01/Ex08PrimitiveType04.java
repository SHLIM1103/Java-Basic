package day01;
// 기본형 데이터타입04
// 논리형 boolean
// 논리형 boolean의 경우 2가지의 값만 존재 -> true / false

public class Ex08PrimitiveType04 {
    public static void main(String[] args) {

        boolean myBool = true ;
        System.out.println("myBool의 현재값: " + myBool);
        myBool = false ;
        System.out.println("myBool의 현재값: " + myBool);

        // 하지만 boolean 데이터타입의 경우에는, 우리가 변수를 직접 만들어서 쓴다기 보다는
        // 연산자 혹은 메소드의 결과값을 그대로 끌어다가 쓰는 경우가 거의 대부분이다.
        
    }
}